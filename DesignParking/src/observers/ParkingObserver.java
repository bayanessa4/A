package observers;

import system.ParkingLot;

/**
 * ממשק המייצג צופה (Observer) שמתעדכן כאשר מתרחש שינוי במצב החניון.
 * <p>
 * דפוס עיצוב: <b>Observer</b> – מאפשר לנתק את לוחות התצוגה (או רכיבים אחרים)
 * מהלוגיקה של החניון, כך שכל שינוי יידע את כל הצופים הרשומים.
 * </p>
 */
public interface ParkingObserver {

    /**
     * מתודה זו מופעלת כאשר יש שינוי במצב החניון,
     * לדוגמה: רכב נכנס או יוצא, ומשפיע על זמינות המקומות.
     *
     * @param lot מופע החניון המעודכן
     */
    void update(ParkingLot lot);
}
